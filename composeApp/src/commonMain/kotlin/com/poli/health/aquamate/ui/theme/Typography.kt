fun TextStyle.withHighEmphasis(): TextStyle = this.copy(color = this.color.copy(alpha = TextOpacity.HIGH_EMPHASIS))
fun TextStyle.withMediumEmphasis(): TextStyle = this.copy(color = this.color.copy(alpha = TextOpacity.MEDIUM_EMPHASIS))
fun TextStyle.withDisabledEmphasis(): TextStyle = this.copy(color = this.color.copy(alpha = TextOpacity.DISABLED_EMPHASIS))
